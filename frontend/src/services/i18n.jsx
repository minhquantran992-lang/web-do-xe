export const LanguageProvider = ({ children }) => {
  const [lang, setLang] = useState('vi');

  useEffect(() => {
    const saved = String(localStorage.getItem(STORAGE_KEY) || '').trim().toLowerCase();
    if (saved === 'en' || saved === 'vi') setLang(saved);
  }, []);

  useEffect(() => {
    localStorage.setItem(STORAGE_KEY, lang);
  }, [lang]);

  const api = useMemo(() => {
    const t = (key) => DICT[lang]?.[key] || DICT.en?.[key] || String(key);
    const toggle = () => setLang((p) => (p === 'vi' ? 'en' : 'vi'));
    const set = (next) => setLang(next === 'en' ? 'en' : 'vi');
    return { lang, t, toggle, set };
  }, [lang]);

  return <LanguageContext.Provider value={api}>{children}</LanguageContext.Provider>;
};

export const useI18n = () => {
  const ctx = useContext(LanguageContext);
  if (!ctx) throw new Error('useI18n must be used inside LanguageProvider');
  return ctx;
};
